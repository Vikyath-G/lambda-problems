#Problem 1: Arithmetic Operations
#Write lambda functions for each of the following arithmetic operations:
#addition, subtraction, multiplication, and division.
#Each lambda function should take two arguments and return the result of the operation.

add=lambda x,y :x+y
sub= lambda x,y :x-y
mul= lambda x,y :x*y
divide=lambda x,y :x/y

print(add(5,8))
print(sub(8,5))
print(mul(5,8))
print(divide(20,4))